# Esse programa, tem como objetivo ler dois números e mostrar a soma entre eles.

# Coleta de dados, para a resolução do desafio.
num1 = int(input('Digite um número: '))
num2 = float(input('Digite outro número: '))
soma = num1 + num2
# Aqui, iremos mostrar o resultado da soma entre num1 + num2.
print('A soma entre {} e {}, vale {}.'.format(num1, num2, soma))
