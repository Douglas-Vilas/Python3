# Esse exercício solicita ler as duas notas de um aluno, e calcular e motrar a média.

# Notas do Aluno
nota1 = float(input('Digite uma nota: '))
nota2 = float(input('Digite outra nota: '))

# Soma das notas e média
media = (nota1 + nota2) / 2

# Exibir o resultado para o usuário
print('O total da nota é: {}.'.format(nota1 + nota2))
print('A média da nota é: {}'.format(media))
